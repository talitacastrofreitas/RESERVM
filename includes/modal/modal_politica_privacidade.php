        <h3>POLÍTICA DE PRIVACIDADE</h3>
        A sua privacidade é importante para nós. É política do RESERVM respeitar a sua privacidade em relação a qualquer informação sua que possamos coletar no sistema RESERVM e outros sistemas que possuímos e operamos.<br><br>
        Solicitamos informações pessoais apenas quando realmente precisamos delas para lhe fornecer um serviço de extensão acadêmica, ao qual essas ficam vinculadas, para o tratamento adequado.<br><br>
        As informações coletadas ficam retidas apenas pelo tempo necessário, de acordo com a finalidade para a qual foram fornecidas. Quando armazenamos dados, fazemos a proteção deles dentro de meios comercialmente aceitáveis para evitar perdas e roubos, bem como acesso, divulgação, cópia, uso ou modificação não autorizados.<br><br>
