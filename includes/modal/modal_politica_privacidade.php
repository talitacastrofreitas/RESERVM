        O uso continuado de nosso sistema será considerado como um consentimento seu de nossas práticas em torno de privacidade e informações pessoais. Se você tiver alguma dúvida sobre como lidamos com dados do usuário e informações pessoais, entre em contato conosco por meio do e-mail <strong>ouvidoria@bahiana.edu.br</strong>.<br><br>
